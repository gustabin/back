public class ComprobanteView {

	/** The cuenta denominacion. */
	private String cuentaDenominacion;

	/** The cuenta nro. */
	private String cuentaNro;

	/** The limite de compra. */
	private String limiteDeCompra;

	/** The nro comprobante. */
	private String nroComprobante;

	/**
	 * Gets the cuenta denominacion.
	 *
	 * @return the cuenta denominacion
	 */
	public String getCuentaDenominacion() {
		return cuentaDenominacion;
	}

	/**
	 * Sets the cuenta denominacion.
	 *
	 * @param cuentaDenominacion
	 *            the new cuenta denominacion
	 */
	public void setCuentaDenominacion(String cuentaDenominacion) {
		this.cuentaDenominacion = cuentaDenominacion;
	}

	/**
	 * Gets the cuenta nro.
	 *
	 * @return the cuenta nro
	 */
	public String getCuentaNro() {
		return cuentaNro;
	}

	/**
	 * Sets the cuenta nro.
	 *
	 * @param cuentaNro
	 *            the new cuenta nro
	 */
	public void setCuentaNro(String cuentaNro) {
		this.cuentaNro = cuentaNro;
	}

	/**
	 * Gets the limite de compra.
	 *
	 * @return the limite de compra
	 */
	public String getLimiteDeCompra() {
		return limiteDeCompra;
	}

	/**
	 * Sets the limite de compra.
	 *
	 * @param limiteDeCompra
	 *            the new limite de compra
	 */
	public void setLimiteDeCompra(String limiteDeCompra) {
		this.limiteDeCompra = limiteDeCompra;
	}

	/**
	 * Gets the nro comprobante.
	 *
	 * @return the nro comprobante
	 */
	public String getNroComprobante() {
		return nroComprobante;
	}

	/**
	 * Sets the nro comprobante.
	 *
	 * @param nroComprobante
	 *            the new nro comprobante
	 */
	public void setNroComprobante(String nroComprobante) {
		this.nroComprobante = nroComprobante;
	}

}
