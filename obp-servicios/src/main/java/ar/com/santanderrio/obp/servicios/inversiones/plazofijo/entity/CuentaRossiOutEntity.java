package ar.com.santanderrio.obp.servicios.inversiones.plazofijo.entity;

public class CuentaRossiOutEntity {
	
	String CNO;
	
	String sucursalRossi;
	
	String sucursalRadicacion;
	
	String retorno;
	
	String codRetorno;
	
	String descripcion;

	public String getCNO() {
		return CNO;
	}

	public void setCNO(String cNO) {
		CNO = cNO;
	}

	public String getSucursalRossi() {
		return sucursalRossi;
	}

	public void setSucursalRossi(String sucursalRossi) {
		this.sucursalRossi = sucursalRossi;
	}

	public String getSucursalRadicacion() {
		return sucursalRadicacion;
	}

	public void setSucursalRadicacion(String sucursalRadicacion) {
		this.sucursalRadicacion = sucursalRadicacion;
	}

	public String getRetorno() {
		return retorno;
	}

	public void setRetorno(String retorno) {
		this.retorno = retorno;
	}

	public String getCodRetorno() {
		return codRetorno;
	}

	public void setCodRetorno(String codRetorno) {
		this.codRetorno = codRetorno;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	

}
