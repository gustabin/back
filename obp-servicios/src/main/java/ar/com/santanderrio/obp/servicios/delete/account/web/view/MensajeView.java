package ar.com.santanderrio.obp.servicios.delete.account.web.view;

/**
 * The Class MensajeView.
 */
public class MensajeView {

	/** The titulo. */
	private String titulo;
	
	/** The template. */
	private String template;

	/**
	 * Gets the titulo.
	 *
	 * @return the titulo
	 */
	public String getTitulo() {
		return titulo;
	}

	/**
	 * Sets the titulo.
	 *
	 * @param titulo
	 *            the new titulo
	 */
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	/**
	 * Gets the template.
	 *
	 * @return the template
	 */
	public String getTemplate() {
		return template;
	}

	/**
	 * Sets the template.
	 *
	 * @param template
	 *            the new template
	 */
	public void setTemplate(String template) {
		this.template = template;
	}
}
