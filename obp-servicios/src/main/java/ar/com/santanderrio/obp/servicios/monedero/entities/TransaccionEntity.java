package ar.com.santanderrio.obp.servicios.monedero.entities;

/**
 * The Class Transaccion.
 */
public class TransaccionEntity {

	/** The id trx. */
	private String idTrx;

	/** The fecha. */
	private String fecha;

	/** The tipo. */
	private String tipo;

	/** The lugar. */
	private String lugar;

	/** The importe. */
	private String importe;

	/** The estado. */
	private String estado;

	/** The orden. */
	private String orden;

	/**
	 * Gets the fecha.
	 *
	 * @return the fecha
	 */
	public String getFecha() {
		return fecha;
	}

	/**
	 * Sets the fecha.
	 *
	 * @param fecha
	 *            the new fecha
	 */
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}

	/**
	 * Gets the tipo.
	 *
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}

	/**
	 * Sets the tipo.
	 *
	 * @param tipo
	 *            the new tipo
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	/**
	 * Gets the lugar.
	 *
	 * @return the lugar
	 */
	public String getLugar() {
		return lugar;
	}

	/**
	 * Sets the lugar.
	 *
	 * @param lugar
	 *            the new lugar
	 */
	public void setLugar(String lugar) {
		this.lugar = lugar;
	}

	/**
	 * Gets the importe.
	 *
	 * @return the importe
	 */
	public String getImporte() {
		return importe;
	}

	/**
	 * Sets the importe.
	 *
	 * @param importe
	 *            the new importe
	 */
	public void setImporte(String importe) {
		this.importe = importe;
	}

	/**
	 * Gets the estado.
	 *
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}

	/**
	 * Sets the estado.
	 *
	 * @param estado
	 *            the new estado
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}

	/**
	 * Gets the id trx.
	 *
	 * @return the id trx
	 */
	public String getIdTrx() {
		return idTrx;
	}

	/**
	 * Sets the id trx.
	 *
	 * @param idTrx
	 *            the new id trx
	 */
	public void setIdTrx(String idTrx) {
		this.idTrx = idTrx;
	}

	/**
	 * Gets the orden.
	 *
	 * @return the orden
	 */
	public String getOrden() {
		return orden;
	}

	/**
	 * Sets the orden.
	 *
	 * @param orden
	 *            the new orden
	 */
	public void setOrden(String orden) {
		this.orden = orden;
	}

}
