package ar.com.santanderrio.obp.servicios.pagos.web.view;

/**
 * The Class UvaView.
 */
public class UvaView {

	/** The label. */
	private String label;

	/** The tasa. */
	private String uva;

	/**
	 * Gets the label.
	 *
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * Sets the label.
	 *
	 * @param label
	 *            the new label
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * Gets the tasa.
	 *
	 * @return the tasa
	 */
	public String getUva() {
		return uva;
	}

	/**
	 * Sets the tasa.
	 *
	 * @param uva
	 *            the new uva
	 */
	public void setUva(String uva) {
		this.uva = uva;
	}

}
