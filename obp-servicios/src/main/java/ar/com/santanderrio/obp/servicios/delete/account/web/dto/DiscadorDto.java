package ar.com.santanderrio.obp.servicios.delete.account.web.dto;

/**
 * @author A308529
 *
 */
public class DiscadorDto {
	
	/**
	 * the codCampania
	 */
	private String CodCampania;
	
	/**
	 * the Nup
	 */
	private String Nup;
	
	/**
	 * the Nombre
	 */
	private String Nombre;
	
	/**
	 * the Apellido
	 */
	private String Apellido;
	
	/**
	 * the TipoDoc
	 */
	private String TipoDoc;
	
	/**
	 * the NumDoc
	 */
	private String NumDoc;
	
	/**
	 * the TelContacto
	 */
	private String TelContacto;
	
	/**
	 * @return the codCampania
	 */
	public String getCodCampania() {
		return CodCampania;
	}
	/**
	 * @param codCampania the codCampania to set
	 */
	public void setCodCampania(String codCampania) {
		CodCampania = codCampania;
	}
	/**
	 * @return the nup
	 */
	public String getNup() {
		return Nup;
	}
	/**
	 * @param nup the nup to set
	 */
	public void setNup(String nup) {
		Nup = nup;
	}
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return Nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	/**
	 * @return the apellido
	 */
	public String getApellido() {
		return Apellido;
	}
	/**
	 * @param apellido the apellido to set
	 */
	public void setApellido(String apellido) {
		Apellido = apellido;
	}
	/**
	 * @return the tipoDoc
	 */
	public String getTipoDoc() {
		return TipoDoc;
	}
	/**
	 * @param tipoDoc the tipoDoc to set
	 */
	public void setTipoDoc(String tipoDoc) {
		TipoDoc = tipoDoc;
	}
	/**
	 * @return the numDoc
	 */
	public String getNumDoc() {
		return NumDoc;
	}
	/**
	 * @param numDoc the numDoc to set
	 */
	public void setNumDoc(String numDoc) {
		NumDoc = numDoc;
	}
	/**
	 * @return the telContacto
	 */
	public String getTelContacto() {
		return TelContacto;
	}
	/**
	 * @param telContacto the telContacto to set
	 */
	public void setTelContacto(String telContacto) {
		TelContacto = telContacto;
	}
	
	
}
