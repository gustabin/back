package ar.com.santanderrio.obp.servicios.tarjetas.reimpresion.entities;

/**
 * The Class TarjetaOut.
 */
public class TarjetaOut {
	
	/** The codigo retorno ext. */
	private int codigoRetornoExt;

	/** The codigo tarjeta. */
	private String codigoTarjeta;

	/** The nro tarjeta. */
	private String nroTarjeta;

	/** The descripcion mensaje. */
	private String descripcionMensaje;

	/** The fecha hora operacion. */
	private String fechaHoraOperacion;

	/**
	 * Gets the codigo tarjeta.
	 *
	 * @return the codigo tarjeta
	 */
	public String getCodigoTarjeta() {
		return codigoTarjeta;
	}

	/**
	 * Sets the codigo tarjeta.
	 *
	 * @param codigoTarjeta
	 *            the new codigo tarjeta
	 */
	public void setCodigoTarjeta(String codigoTarjeta) {
		this.codigoTarjeta = codigoTarjeta;
	}

	/**
	 * Gets the nro tarjeta.
	 *
	 * @return the nro tarjeta
	 */
	public String getNroTarjeta() {
		return nroTarjeta;
	}

	/**
	 * Sets the nro tarjeta.
	 *
	 * @param nroTarjeta
	 *            the new nro tarjeta
	 */
	public void setNroTarjeta(String nroTarjeta) {
		this.nroTarjeta = nroTarjeta;
	}

	/**
	 * Gets the descripcion mensaje.
	 *
	 * @return the descripcion mensaje
	 */
	public String getDescripcionMensaje() {
		return descripcionMensaje;
	}

	/**
	 * Sets the descripcion mensaje.
	 *
	 * @param descripcionMensaje
	 *            the new descripcion mensaje
	 */
	public void setDescripcionMensaje(String descripcionMensaje) {
		this.descripcionMensaje = descripcionMensaje;
	}

	/**
	 * Gets the fecha hora operacion.
	 *
	 * @return the fecha hora operacion
	 */
	public String getFechaHoraOperacion() {
		return fechaHoraOperacion;
	}

	/**
	 * Sets the fecha hora operacion.
	 *
	 * @param fechaHoraOperacion
	 *            the new fecha hora operacion
	 */
	public void setFechaHoraOperacion(String fechaHoraOperacion) {
		this.fechaHoraOperacion = fechaHoraOperacion;
	}

	/**
	 * Gets the codigo retorno ext.
	 *
	 * @return the codigo retorno ext
	 */
	public int getCodigoRetornoExt() {
		return codigoRetornoExt;
	}

	/**
	 * Sets the codigo retorno ext.
	 *
	 * @param codigoRetornoExt
	 *            the new codigo retorno ext
	 */
	public void setCodigoRetornoExt(int codigoRetornoExt) {
		this.codigoRetornoExt = codigoRetornoExt;
	}
	
	

}
