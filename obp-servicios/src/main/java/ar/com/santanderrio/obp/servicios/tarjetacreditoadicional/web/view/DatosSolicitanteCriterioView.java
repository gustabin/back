package ar.com.santanderrio.obp.servicios.tarjetacreditoadicional.web.view;

/**
 * The Class DatosSolicitanteCriterioView.
 */
public class DatosSolicitanteCriterioView {

    /** The doc tipo. */
    private String docTipo;

    /** The doc. */
    private String doc;

    /** The fecha nacimiento. */
    private String fechaNacimiento;

    /**
     * Gets the doc tipo.
     *
     * @return the docTipo
     */
    public String getDocTipo() {
        return docTipo;
    }

    /**
     * Sets the doc tipo.
     *
     * @param docTipo
     *            the docTipo to set
     */
    public void setDocTipo(String docTipo) {
        this.docTipo = docTipo;
    }

    /**
     * Gets the doc.
     *
     * @return the doc
     */
    public String getDoc() {
        return doc;
    }

    /**
     * Sets the doc.
     *
     * @param doc
     *            the doc to set
     */
    public void setDoc(String doc) {
        this.doc = doc;
    }

    /**
     * Gets the fecha nacimiento.
     *
     * @return the fechaNacimiento
     */
    public String getFechaNacimiento() {
        return fechaNacimiento;
    }

    /**
     * Sets the fecha nacimiento.
     *
     * @param fechaNacimiento
     *            the fechaNacimiento to set
     */
    public void setFechaNacimiento(String fechaNacimiento) {
        this.fechaNacimiento = fechaNacimiento;
    }

}
