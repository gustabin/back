package ar.com.santanderrio.obp.servicios.titulos.operaciones.view;

/**
 * The Class IntervinienteView.
 */
public class IntervinienteView {
    
    /** The nombre. */
    private String nombre;
    
    /** The apellido. */
    private String apellido;
    
    /**
	 * Gets the nombre.
	 *
	 * @return the nombre
	 */
    public String getNombre() {
        return nombre;
    }
    
    /**
	 * Sets the nombre.
	 *
	 * @param nombre
	 *            the new nombre
	 */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    /**
	 * Gets the apellido.
	 *
	 * @return the apellido
	 */
    public String getApellido() {
        return apellido;
    }
    
    /**
	 * Sets the apellido.
	 *
	 * @param apellido
	 *            the new apellido
	 */
    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

}
