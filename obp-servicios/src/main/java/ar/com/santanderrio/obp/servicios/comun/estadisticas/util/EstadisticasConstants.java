	/**
	 * Gets the cod tipo transacion movs a conf from tipo cuenta.
	 * 
	 * @param tipoCuenta the tipo cuenta
	 * @return the cod tipo transacion movs a conf from tipo cuenta
	 */
	public static String getCodTipoTransacionMovsAConfFromTipoCuenta(TipoCuenta tipoCuenta) {

		if (TipoCuenta.CAJA_AHORRO_DOLARES.equals(tipoCuenta)) {
			return CODIGO_TRANSACCION_ESTADISTICAS_MOVS_A_CONF_CA_DOLARES;
		}

		if (TipoCuenta.CAJA_AHORRO_PESOS.equals(tipoCuenta)) {
			return CODIGO_TRANSACCION_ESTADISTICAS_MOVS_A_CONF_CA_PESOS;
		}

		if (TipoCuenta.CUENTA_UNICA.equals(tipoCuenta)) {
			return CODIGO_TRANSACCION_ESTADISTICAS_MOVS_A_CONF_CU;
		}

		return codigoTransaccionCuentaCorriente(tipoCuenta);
	}

	/**
	 * Codigo transaccion cuenta corriente.
	 * 
	 * @param tipoCuenta the tipo cuenta
	 * @return the string
	 */
	private static String codigoTransaccionCuentaCorriente(TipoCuenta tipoCuenta) {
		if (TipoCuenta.CUENTA_CORRIENTE_DOLARES.equals(tipoCuenta)) {
			return CODIGO_TRANSACCION_ESTADISTICAS_MOVS_A_CONF_CC_DOLARES;
		}
		if (TipoCuenta.CUENTA_CORRIENTE_PESOS.equals(tipoCuenta)) {
			return CODIGO_TRANSACCION_ESTADISTICAS_MOVS_A_CONF_CC_PESOS;
		}
		return "";
	}
