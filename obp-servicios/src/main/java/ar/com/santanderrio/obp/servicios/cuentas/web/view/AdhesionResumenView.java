package ar.com.santanderrio.obp.servicios.cuentas.web.view;

/**
 * The Class AdhesionResumenView.
 *
 * @author
 */
public class AdhesionResumenView {

	/** The numero cuenta. */
	private String numeroCuenta;

	/** The opinion usuario. */
	private String opinionUsuario;

	/** The resumen online. */
	private boolean resumenOnline;

	/** The numero comprobante. */
	private String numeroComprobante;

	/** The fecha. */
	private String fecha;

	/** The hora. */
	private String hora;

	/** The mensaje. */
	private String mensaje;

	/**
	 * Gets the numero cuenta.
	 *
	 * @return the numero cuenta
	 */
	public String getNumeroCuenta() {
		return numeroCuenta;
	}

	/**
	 * Sets the numero cuenta.
	 *
	 * @param numeroCuenta
	 *            the new numero cuenta
	 */
	public void setNumeroCuenta(String numeroCuenta) {
		this.numeroCuenta = numeroCuenta;
	}

	/**
	 * Gets the opinion usuario.
	 *
	 * @return the opinion usuario
	 */
	public String getOpinionUsuario() {
		return opinionUsuario;
	}

	/**
	 * Sets the opinion usuario.
	 *
	 * @param opinionUsuario
	 *            the new opinion usuario
	 */
	public void setOpinionUsuario(String opinionUsuario) {
		this.opinionUsuario = opinionUsuario;
	}

	/**
	 * Checks if is resumen online.
	 *
	 * @return true, if is resumen online
	 */
	public boolean isResumenOnline() {
		return resumenOnline;
	}

	/**
	 * Sets the resumen online.
	 *
	 * @param resumenOnline
	 *            the new resumen online
	 */
	public void setResumenOnline(boolean resumenOnline) {
		this.resumenOnline = resumenOnline;
	}

	/**
	 * Gets the numero comprobante.
	 *
	 * @return the numero comprobante
	 */
	public String getNumeroComprobante() {
		return numeroComprobante;
	}

	/**
	 * Sets the numero comprobante.
	 *
	 * @param numeroComprobante
	 *            the new numero comprobante
	 */
	public void setNumeroComprobante(String numeroComprobante) {
		this.numeroComprobante = numeroComprobante;
	}

	/**
	 * Gets the fecha.
	 *
	 * @return the fecha
	 */
	public String getFecha() {
		return fecha;
	}

	/**
	 * Sets the fecha.
	 *
	 * @param fecha
	 *            the new fecha
	 */
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}

	/**
	 * Gets the hora.
	 *
	 * @return the hora
	 */
	public String getHora() {
		return hora;
	}

	/**
	 * Sets the hora.
	 *
	 * @param hora
	 *            the new hora
	 */
	public void setHora(String hora) {
		this.hora = hora;
	}

	/**
	 * Gets the mensaje.
	 *
	 * @return the mensaje
	 */
	public String getMensaje() {
		return mensaje;
	}

	/**
	 * Sets the mensaje.
	 *
	 * @param mensaje
	 *            the new mensaje
	 */
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}

}
