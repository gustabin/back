package ar.com.santanderrio.obp.servicios.marcaviajero.entities;

/**
 * The Class PaisDTO.
 */
public class PaisDTO {

	/** The cod pais. */
	private String codPais;

	/** The descripcion pais. */
	private String descripcionPais;

	/**
	 * Gets the cod pais.
	 *
	 * @return the cod pais
	 */
	public String getCodPais() {
		return codPais;
	}

	/**
	 * Sets the cod pais.
	 *
	 * @param codPais the new cod pais
	 */
	public void setCodPais(String codPais) {
		this.codPais = codPais;
	}

	/**
	 * Gets the descripcion pais.
	 *
	 * @return the descripcion pais
	 */
	public String getDescripcionPais() {
		return descripcionPais;
	}

	/**
	 * Sets the descripcion pais.
	 *
	 * @param descripcionPais the new descripcion pais
	 */
	public void setDescripcionPais(String descripcionPais) {
		this.descripcionPais = descripcionPais;
	}

}
