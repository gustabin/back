package ar.com.santanderrio.obp.servicios.inversiones.titulos.infomercado.view;

/**
 * The Class IndiceView.
 */
public class IndiceView {

	/** The nombre. */
	private String nombre;
	
	/** The ultimo. */
	private String ultimo;
	
	/** The indicador. */
	private String indicador;
	
	/** The valor. */
	private String valor;

	/**
	 * Gets the nombre.
	 *
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * Sets the nombre.
	 *
	 * @param nombre
	 *            the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * Gets the ultimo.
	 *
	 * @return the ultimo
	 */
	public String getUltimo() {
		return ultimo;
	}

	/**
	 * Sets the ultimo.
	 *
	 * @param ultimo
	 *            the ultimo to set
	 */
	public void setUltimo(String ultimo) {
		this.ultimo = ultimo;
	}

	/**
	 * Gets the indicador.
	 *
	 * @return the indicador
	 */
	public String getIndicador() {
		return indicador;
	}

	/**
	 * Sets the indicador.
	 *
	 * @param indicador
	 *            the indicador to set
	 */
	public void setIndicador(String indicador) {
		this.indicador = indicador;
	}

	/**
	 * Gets the valor.
	 *
	 * @return the valor
	 */
	public String getValor() {
		return valor;
	}

	/**
	 * Sets the valor.
	 *
	 * @param valor
	 *            the valor to set
	 */
	public void setValor(String valor) {
		this.valor = valor;
	}

}
