package ar.com.santanderrio.obp.servicios.inversiones.plazofijo.view;

/**
 * The Class ValoresTasa.
 */
public class ValoresTasa {

	/** The dias. */
	String dias;

	/** The tasa nominal basica. */
	String tasaNominalBasica;

	/** The penalizacion. */
	String penalizacion;

	/** The moneda. */
	String moneda;

	/** The importe. */
	String importe;

	/**
	 * Gets the dias.
	 *
	 * @return the dias
	 */
	public String getDias() {
		return dias;
	}

	/**
	 * Sets the dias.
	 *
	 * @param dias
	 *            the new dias
	 */
	public void setDias(String dias) {
		this.dias = dias;
	}

	/**
	 * Gets the tasa nominal basica.
	 *
	 * @return the tasa nominal basica
	 */
	public String getTasaNominalBasica() {
		return tasaNominalBasica;
	}

	/**
	 * Sets the tasa nominal basica.
	 *
	 * @param tasaNominalBasica
	 *            the new tasa nominal basica
	 */
	public void setTasaNominalBasica(String tasaNominalBasica) {
		this.tasaNominalBasica = tasaNominalBasica;
	}

	/**
	 * Gets the penalizacion.
	 *
	 * @return the penalizacion
	 */
	public String getPenalizacion() {
		return penalizacion;
	}

	/**
	 * Sets the penalizacion.
	 *
	 * @param penalizacion
	 *            the new penalizacion
	 */
	public void setPenalizacion(String penalizacion) {
		this.penalizacion = penalizacion;
	}

	/**
	 * Gets the moneda.
	 *
	 * @return the moneda
	 */
	public String getMoneda() {
		return moneda;
	}

	/**
	 * Sets the moneda.
	 *
	 * @param moneda
	 *            the new moneda
	 */
	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}

	/**
	 * Gets the importe.
	 *
	 * @return the importe
	 */
	public String getImporte() {
		return importe;
	}

	/**
	 * Sets the importe.
	 *
	 * @param importe the new importe
	 */
	public void setImporte(String importe) {
		this.importe = importe;
	}

}
