package ar.com.santanderrio.obp.servicios.cuentas.web.view;

/**
 * The Class PlazoPDCView.
 */
public class PlazoPDCView {

	/** The plazo. */
	private String plazo;
	
	/** The saldo PDC. */
	private double saldoPDC;

	/**
	 * Gets the plazo.
	 *
	 * @return the plazo
	 */
	public String getPlazo() {
		return plazo;
	}

	/**
	 * Sets the plazo.
	 *
	 * @param plazo
	 *            the plazo to set
	 */
	public void setPlazo(String plazo) {
		this.plazo = plazo;
	}

	/**
	 * Gets the saldo PDC.
	 *
	 * @return the saldoPDC
	 */
	public double getSaldoPDC() {
		return saldoPDC;
	}

	/**
	 * Sets the saldo PDC.
	 *
	 * @param saldoPDC
	 *            the saldoPDC to set
	 */
	public void setSaldoPDC(double saldoPDC) {
		this.saldoPDC = saldoPDC;
	}

}
