package ar.com.santanderrio.obp.servicios.comun.combos.entities;

/**
 * The Class Opcion.
 */
public class Opcion {

	/** The id. */
	private String id;

	/** The opcion. */
	private String opcion;

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Setter para id.
	 *
	 * @param id
	 *            el nuevo id
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * Gets the opcion.
	 *
	 * @return the opcion
	 */
	public String getOpcion() {
		return opcion;
	}

	/**
	 * Sets the opcion.
	 *
	 * @param opcion
	 *            the new opcion
	 */
	public void setOpcion(String opcion) {
		this.opcion = opcion;
	}
}
