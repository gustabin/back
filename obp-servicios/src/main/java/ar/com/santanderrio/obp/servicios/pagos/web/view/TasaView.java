package ar.com.santanderrio.obp.servicios.pagos.web.view;

/**
 * The Class TasaView.
 */
public class TasaView {

	/** The label. */
	private String label;

	/** The tasa. */
	private String tasa;

	/**
	 * Gets the label.
	 *
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * Sets the label.
	 *
	 * @param label
	 *            the new label
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * Gets the tasa.
	 *
	 * @return the tasa
	 */
	public String getTasa() {
		return tasa;
	}

	/**
	 * Sets the tasa.
	 *
	 * @param tasa
	 *            the new tasa
	 */
	public void setTasa(String tasa) {
		this.tasa = tasa;
	}

}
