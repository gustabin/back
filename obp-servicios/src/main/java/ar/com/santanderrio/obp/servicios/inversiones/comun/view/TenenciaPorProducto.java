public class TenenciaPorProducto {

	/** The total. */
	private String total;

	/** The producto. */
	private String producto;

	/** The tenencia valuada al costo. */
	private String tenenciaValuadaAlCosto;

	/** The tenencia valuada A hoy. */
	private String tenenciaValuadaAHoy;

	/** The resultado. */
	private String resultado;

	/** The moneda. */
	private String moneda;

	/**
	 * Gets the total.
	 *
	 * @return the total
	 */
	public String getTotal() {
		return total;
	}

	/**
	 * Sets the total.
	 *
	 * @param total
	 *            the total to set
	 */
	public void setTotal(String total) {
		this.total = total;
	}

	/**
	 * Gets the producto.
	 *
	 * @return the producto
	 */
	public String getProducto() {
		return producto;
	}

	/**
	 * Sets the producto.
	 *
	 * @param producto
	 *            the producto to set
	 */
	public void setProducto(String producto) {
		this.producto = producto;
	}

	/**
	 * Gets the tenencia valuada al costo.
	 *
	 * @return the tenenciaValuadaAlCosto
	 */
	public String getTenenciaValuadaAlCosto() {
		return tenenciaValuadaAlCosto;
	}

	/**
	 * Sets the tenencia valuada al costo.
	 *
	 * @param tenenciaValuadaAlCosto
	 *            the tenenciaValuadaAlCosto to set
	 */
	public void setTenenciaValuadaAlCosto(String tenenciaValuadaAlCosto) {
		this.tenenciaValuadaAlCosto = tenenciaValuadaAlCosto;
	}

	/**
	 * Gets the tenencia valuada A hoy.
	 *
	 * @return the tenenciaValuadaAHoy
	 */
	public String getTenenciaValuadaAHoy() {
		return tenenciaValuadaAHoy;
	}

	/**
	 * Sets the tenencia valuada A hoy.
	 *
	 * @param tenenciaValuadaAHoy
	 *            the tenenciaValuadaAHoy to set
	 */
	public void setTenenciaValuadaAHoy(String tenenciaValuadaAHoy) {
		this.tenenciaValuadaAHoy = tenenciaValuadaAHoy;
	}

	/**
	 * Gets the resultado.
	 *
	 * @return the resultado
	 */
	public String getResultado() {
		return resultado;
	}

	/**
	 * Sets the resultado.
	 *
	 * @param resultado
	 *            the resultado to set
	 */
	public void setResultado(String resultado) {
		this.resultado = resultado;
	}

	/**
	 * Gets the moneda.
	 *
	 * @return the moneda
	 */
	public String getMoneda() {
		return moneda;
	}

	/**
	 * Sets the moneda.
	 *
	 * @param moneda
	 *            the moneda to set
	 */
	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}

}
